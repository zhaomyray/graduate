package edu.zut.pt.pojo;

public class Week {

    private int id;
    private int weekMessage;
    private int weekValue;

    public void setId(int id) {
        this.id = id;
    }

    public void setWeekMessage(int weekMessage) {
        this.weekMessage = weekMessage;
    }

    public void setWeekValue(int weekValue) {
        this.weekValue = weekValue;
    }

    public int getId() {
        return id;
    }

    public int getWeekMessage() {
        return weekMessage;
    }

    public int getWeekValue() {
        return weekValue;
    }
}
